a = 5
b = 3
#Arithmetic Operator
print(a + b)
print(a - b)
print(a * b)
print(a / b) #always comes in float datatype
print(a % b) #modulus operator for reminder
print(a ** b) #a^b Power of


#Relational | Comparison Operator
a = 50
b = 20
#return boolean  value
print(a == b) #False
print(a != b) #True
print(a > b) #True
print(a >= b) #True
print(a < b) #False
print(a <= b) #False


#Assignment Operators
num = 10 #normal assignment
num += 10 #addition and assign
num -= 10 #Subtract and assign
num *= 10 #multiply and assign
num /= 5 #divide and assign
num %= 2 #Modulo division and assign
num **= 10 #use powerof and then assign

print(num)





